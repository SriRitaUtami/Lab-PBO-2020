/**
* The HelloWorldApp class implements an application
* that displays "Hello World!" to the standard output
*/
public class HelloWorldApp {
public static void main(String[] args) {
// Display "Hello World!"
System.out.println("Hello World!");
}
}
//Dalam program ini yang dihasilkan adalah output berupa "Hello World" seperti yang telah terdeteksi oleh program
//Class yang berada dalam program ini bernama HelloWorldApp dengan public static void main(String[] args) adalah method main nya
//Untuk menghasilkan output berupa line pada java kita membutuhkan syntax/code yaitu System.out.println("");